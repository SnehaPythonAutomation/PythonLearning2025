# +	Addition	x + y
# -	Subtraction	x - y
# *	Multiplication	x * y
# /	Division	x / y
# %	Modulus	x % y
# **	Exponentiation	x ** y
# //	Floor division	x // y (returns the quotient, discarding the remainder)

a=int(input("Enter the value of a\n"))
b=int(input("Enter the value of b\n"))

print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a%b)  # modules returns reminder
print(a**b) # 10^3
print(a//b) # returns the quotient
